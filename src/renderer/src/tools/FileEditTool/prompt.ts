export function getFileEditPrompt(): string {
  return `# Edit 工具使用说明

替换文件中的文本。支持精准匹配或整体替换，自动检测编码（UTF-8/UTF-16）。

## 使用场景
- 修改已有文件的特定部分（修复 bug、更新配置、重构代码）
- **不要**用此工具创建新文件（用 Write）

## Hashline 锚点配合
Edit 必须配合 Read 工具返回的 **Hashline 锚点**使用：
1. **先 Read 后 Edit**：先用 Read 获取文件最新内容与 hashline 锚点
2. **old_string 取值**：取 hashline 中 \`|\` 后面的部分（行内容），**不含行号和哈希前缀**
3. **hashline 参数**（可选）：填写 \`行号 哈希\`（如 \`42 abc1234\`），用于交叉验证
4. **匹配失败处理**：如果 Edit 返回错误（如未找到 \`old_string\`），**重新 Read 文件**获取当前实际内容与最新锚点，不要猜测内容

## 核心规则
1. **先 Read 后 Edit**：执行 Edit 前，务必先用 Read 工具确认文件内容与最新锚点
2. **唯一匹配**：确保 \`old_string\` 在文件中唯一且精确匹配（包括缩进、空格、换行符）
3. **只改最小范围**：\`old_string\` 应尽量精确（匹配要替换的几行而非整个文件），避免意外替换其他位置
4. **路径**：使用绝对路径，正斜杠 \`/\` 或 Windows 反斜杠 \`\\\\\` 均可

## replace_all 参数
- 默认 \`false\`（只替换第一个匹配）
- 设置为 \`true\` 时替换文件中所有匹配项
- 仅在确认要**批量替换**时使用（如重命名变量名）

## 编码说明
- 自动识别 UTF-8 / UTF-16 LE / UTF-16 BE
- 编辑后保持原编码

## 最佳实践
- 优先用此工具而非 sed/awk 等 shell 命令
- 对 \`old_string\` 中的引号、模板字符串（\`\${}\`）等特殊字符，复制 Read 返回的 \`|\` 后面的原文即可，工具会自动做引号归一化匹配
- 使用 \`hashline\` 参数可以交叉验证定位准确性，避免因行号漂移导致的误改`
}
