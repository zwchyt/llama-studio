export function getTodoWritePrompt(): string {
  return `# TodoWrite 工具使用说明

写入/替换当前会话的**完整待办清单**。每调用一次都会覆盖上一次的清单，因此请每次都传入最新、完整的列表。

## 适用场景
- 复杂多步任务（≥3 步）开始前做计划
- 用户明确要求使用待办清单时
- 接到一串任务（编号 / 逗号分隔）时立即记录
- 开始某项任务前把它标记为 in_progress
- 完成某项后标记为 completed，并补充新发现的子任务

## 不适用
- 只有单一、琐碎的任务时，直接做即可，不必调用

## 字段
- content: 简短、可执行的描述（祈使句，如“修复登录鉴权 bug”）
- status: pending / in_progress / completed
- activeForm（可选）: 进行中时在状态栏展示的现在分词形式（如“正在修复登录鉴权”）

## 最佳实践
- 描述清晰具体，便于后续跟踪
- 调用前先确认没有重复项
- 本工具与 Task* 系列共享同一份任务清单（只是表达形式更简洁）`
}
