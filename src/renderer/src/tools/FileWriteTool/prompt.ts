export function getFileWritePrompt(): string {
  return `# Write 工具使用说明

仅用于**新建文件**，自动创建父目录。

## 使用场景
- 创建新文件（目标路径尚不存在）

## 核心规则（强制）
1. **已存在的文件禁止用 Write 重写**：若目标文件已存在且非空，Write 会被系统**直接拒绝**。修改已有文件一律用 **Edit**：先 Read 获取最新 hashline，再用 Edit 只替换需改动的片段，**不要把整文件重写**。
2. **删除文件用 Delete，不是 Write**：不要用 Write 写脚本去删文件——直接用 **Delete** 工具
3. **确需整体替换时**：极少数确需把整个已有文件推倒重建的情况，先用 Delete 删除再用 Write 新建；否则一律用 Edit
4. **路径**：使用绝对路径，正斜杠 \`/\` 或反斜杠 \`\\\\\` 均可
5. **编码**：写入 UTF-8 编码，保持文件一致性

## 最佳实践
- 优先用此工具而非 echo/重定向等 shell 命令
- 写入前先用 Glob 或 Read 确认目标位置（已存在则改用 Edit）
- 批量写文件时，每次调用只写一个文件`
}
