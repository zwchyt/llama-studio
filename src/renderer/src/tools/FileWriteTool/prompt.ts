export function getFileWritePrompt(): string {
  return `# Write 工具使用说明

将内容写入文件（完全覆盖已有文件或创建新文件），自动创建父目录。

## 使用场景
- 创建新文件
- 完全覆盖已有文件（注意：不是追加，会清空原文）

## 核心规则
1. **修改文件用 Edit，不是 Write**：如果你想修改已有文件的特定部分而不是整体替换，使用 **Edit** 工具而非 Write
2. **删除文件用 Delete，不是 Write**：不要用 Write 写一段脚本去删文件——直接使用 **Delete** 工具
3. **写入前确认**：如目标文件已存在且你只修改其中几行，先用 Read 查看，再用 Edit；不要用 Write 覆盖
4. **路径**：使用绝对路径，正斜杠 \`/\` 或反斜杠 \`\\\\\` 均可
4. **编码**：写入 UTF-8 编码，保持文件一致性

## 最佳实践
- 优先用此工具而非 echo/重定向等 shell 命令
- 写入前先用 Glob 或 Read 确认目标位置
- 批量写文件时，每次调用只写一个文件`
}
