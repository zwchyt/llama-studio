export function getGlobPrompt(): string {
  return `# Glob 工具使用说明

按文件名模式（glob）查找**文件**（不支持目录匹配），返回**绝对路径**。

## 使用场景
- 查找特定命名的文件（如 \`*.ts\`、\`*.json\`）
- 确认文件是否存在
- 当你知道文件名的一部分时快速定位

## 注意事项
- ⚠️ **不要用 \`*\` 或 \`**\` 单独搜索**：这会导致列出大量文件并被截断，对模型无用
- **不匹配目录**：pattern 如 \`*/\` 不会返回目录名
- 优先使用更具体的 pattern，如 \`src/**/*.ts\` 而非 \`**/*.ts\`
- 返回的是**绝对路径**
- 搜索结果可能截断，注意 truncation 提示
- 默认搜索根为当前项目目录

## 替代方案
- 列出当前目录下的文件和子目录 → 使用 **Bash** 工具执行 \`dir /b\`
- 按文件内容搜索 → 使用 **Grep** 工具
- 查看文件内容 → 使用 **Read** 工具
- 浏览目录树 → 使用工作台侧边栏的**文件树**`
}
