export function getFileReadPrompt(): string {
  return `# Read 工具使用说明

读取文件内容并返回（自动检测编码 UTF-8/UTF-16，行号每 10 行显示一次以节省 token），返回绝对路径。

## 使用场景
- 查看文件内容时**必须**优先使用此工具（禁止用 Bash 的 type/cat）
- 代码审查、配置检查、日志排查
- Edit 前确认文件当前内容

## 核心规则
1. **编码**：自动识别 UTF-8 / UTF-16 LE / UTF-16 BE
2. **行号显示**：每 10 行显示一次行号（第 1、11、21…行），其余用 \`:\` 占位，节省 token
3. **分片读取**：支持 \`offset\`（1-indexed，负数从末尾计数）和 \`limit\`（最大行数）参数
4. **token 预算**：约 25000 token 上限，超限会提示改用 Grep 搜索
5. **路径**：使用绝对路径，正斜杠 \`/\` 或反斜杠 \`\\\\\` 均可
6. **Read → Edit 流程**：编辑前必须 Read 获取当前内容，确保 \`old_string\` 精确匹配

## 最佳实践
- 如不确定文件路径，先用 Glob 查找
- 查看大文件时指定 offset/limit 分片读取，或使用 Grep 按关键字搜索
- 查看文件末尾内容：offset=-50（读取最后 50 行）`
}
