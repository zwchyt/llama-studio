export function getFileReadPrompt(): string {
  return `# Read 工具使用说明

读取文件内容并返回（自动检测编码 UTF-8/UTF-16，带行号），返回绝对路径。

## 使用场景
- 查看文件内容时**必须**优先使用此工具（禁止用 Bash 的 type/cat）
- 代码审查、配置检查、日志排查
- Edit 前确认文件当前内容

## 核心规则
1. **编码**：自动识别 UTF-8 / UTF-16 LE / UTF-16 BE
2. **大文件截断**：超过 128KB 自动截断，注意 \`truncated\` 标记
3. **路径**：使用绝对路径，正斜杠 \`/\` 或反斜杠 \`\\\\\` 均可
4. **Read → Edit 流程**：编辑前必须 Read 获取当前内容，确保 \`old_string\` 精确匹配

## 最佳实践
- 如不确定文件路径，先用 Glob 查找
- 查看日志文件时注意截断提示，可能需要 Grep 按关键字搜索`
}
