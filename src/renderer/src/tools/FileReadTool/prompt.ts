export function getFileReadPrompt(): string {
  return `# Read 工具使用说明

读取文件内容并返回，每行附带 **Hashline 锚点**（行号 + 内容指纹哈希）。

格式：\`行号 7位哈希|行内容\`

示例：
\`\`\`
42 abc1234|def func() {
43 def5678|  return x
44 8901ab|}
\`\`\`

## Hashline 锚点规则
1. **锚点由 Read 工具自动生成**，基于每行内容计算 FNV-1a 哈希前 7 位
2. **模型不可自行编造或修改锚点字符串**——锚点是定位凭证，篡改会导致 Edit 失败
3. 如果同一行内容不变，多次 Read 得到的哈希相同
4. Edit 时用 \`old_string\` 匹配整行内容（不含行号和哈希前缀），锚点仅用于辅助验证

## 使用场景
- 查看文件内容时**必须**优先使用此工具（禁止用 Bash 的 type/cat）
- 代码审查、配置检查、日志排查
- Edit 前确认文件当前内容（获取最新锚点）

## 核心规则
1. **编码**：自动识别 UTF-8 / UTF-16 LE / UTF-16 BE
2. **分片读取**：支持 \`offset\`（1-indexed，负数从末尾计数）和 \`limit\`（最大行数）参数
3. **token 预算**：约 25000 token 上限，超限会提示改用 Grep 搜索
4. **路径**：使用绝对路径，正斜杠 \`/\` 或反斜杠 \`\\\\\` 均可
5. **Read → Edit 流程**：编辑前必须 Read 获取最新内容与锚点，\`old_string\` 必须精确匹配整行内容（不含行号和哈希）

## 最佳实践
- 如不确定文件路径，先用 Glob 查找
- 查看大文件时指定 offset/limit 分片读取，或使用 Grep 按关键字搜索
- 查看文件末尾内容：offset=-50（读取最后 50 行）
- Edit 前务必 Read 获取最新的哈希锚点，不要使用之前读取的旧锚点（行内容变更后哈希会变）`
}
