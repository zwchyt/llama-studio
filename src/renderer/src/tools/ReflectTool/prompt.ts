export function getReflectPrompt(): string {
  return `# Reflect 工具使用说明

在多步任务中主动暂停并做一次结构化自省。此工具**无副作用**（不读写文件），只记录你的反思并原样回执，帮助你校准方向。

## 适用场景（严格限定）
- 多步任务（≥3 步）中**遇到阻碍**、卡壳，或连续失败后需要重新规划
- **阶段切换**时（如从"排查"进入"修复"）确认方向

## 禁止
- **禁止每轮调用**、禁止用于常规进度汇报
- 不要用它代替 TodoWrite 的任务状态维护

## 字段
- assessment（必填）：对当前进展/现状的判断（已完成什么、是否偏离目标）
- blockers（可选）：当前阻碍或不确定点
- next_steps（必填）：据此得出的下一步具体计划

## 提示
- 反思后应立即据 next_steps 继续执行；若计划有变，用 TodoWrite 更新任务`
}
