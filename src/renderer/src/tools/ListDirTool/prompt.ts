import { LIST_DIR_TOOL_NAME } from './constants'

export function getListDirPrompt(): string {
  return `# ${LIST_DIR_TOOL_NAME} 工具使用说明

列出指定目录下的文件和子目录，返回树状视图。

## 使用场景
- 在操作前确认某一个目录下的文件/子目录是否存在（单层确认）
- 核对路径（Write/Read 前确认父目录）
- 浏览「单个目录层级」的内容以确定下一步操作

## 输出格式
- 目录以 \`/\` 结尾，并显示其中的条目数
- 文件直接显示文件名
- 超过 1000 项时会被截断

## 注意事项
- path 为可选参数，省略时默认为项目根目录
- 支持相对路径和绝对路径
- 只列出「单个目录层级」内容，不会递归展开子目录
- **仅用于查看单个目录的即时内容**；不要逐子目录调用 ListDir 来"分析项目"
- 需要全局概览 / 分析目录功能，请用 AnalyzeDir 工具（一次出全树）
- 如需查找特定文件，优先使用 Glob 工具`
}
