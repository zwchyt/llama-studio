export function getGrepPrompt(): string {
  return `# Grep 工具使用说明

按正则表达式搜索文件内容，支持计数、文件类型/glob 过滤和上下文行显示。返回**绝对路径**。

## 使用场景
- 在项目中搜索特定代码模式（函数定义、引用、TODO 标记等）
- 统计分析匹配数量
- 替代 shell 的 \`findstr\` / \`grep\`（禁止用 Bash 做文本搜索）

## 核心规则
1. **禁止用 Bash 替代**：搜索文件内容必须用 Grep 工具，严禁用 \`findstr\` 或 Bash 管道
2. **正则语法**：使用 JavaScript 风格正则，注意转义（\`\\d\` 表示数字，\`\\.\` 匹配点号）
3. **大小写**：默认区分大小写；设置 \`-i: true\` 可忽略大小写
4. **返回路径**：返回的是**绝对路径**
5. **结果截断**：默认最多返回 250 行；通过 \`head_limit\` 调整上限（0=不限）
6. **每行截断**：超过 1000 字符的行会被截断，避免输出膨胀
7. **超时保护**：搜索超过 20 秒会自动终止，返回部分结果

## 输出模式
- \`files_with_matches\`（默认）：只列出匹配的文件名
- \`content\`：显示匹配行及其行号
- \`count\`：显示每个文件的匹配数量

## 文件过滤
- \`glob\`：精确 glob 模式，如 \`"*.ts"\`、\`"src/**/*.tsx"\`
- \`type\`：文件类型快捷方式，如 \`"ts"\`、\`"py"\`、\`"rs"\`、\`"go"\`、\`"java"\`、\`"md"\` 等（自动映射到 glob）
- 两者都不传则在**项目目录**下全量搜索

## 最佳实践
- 先用 count 模式了解匹配规模，再用 content 模式查看详情
- 搜索范围太大时加 \`type\` 或 \`glob\` 缩小范围
- 善用 \`context\` 参数显示匹配行上下文（如 \`context: 2\` 显示前后各 2 行）
- 搜索超大项目时建议指定 \`type\` 避免超时截断`
}
